            <h2>Arquitetura do Magento</h2>
            
            <p>Uma das principais características da plataforma Magento é sua arquitetura modular, ou seja o sistema como um todo é composto por diversos módulos independentes que reunidos fazem compõem o sistema. Para quem ainda não sabe o que é Magento e qual a diferença que isso faz em termos técnicos, posso garantir que esse é um dos pontos chave para o sucesso da plataforma, onde instalamos e desinstalamos um bloco de programação e/ou funcionalidade sem que seja prejudicado o restante do site.</p>
            
            <p>Os módulos são conhecidos como Extensões Magento, ou plugins para Magento e são um dos pontos fortes da plataforma. através deles podemos incorporar novas funções à loja e com isso torná-la mais interessante para que a visita e também mais funcional para quem administra, facilitando muito o trabalho do dono do site e do usuário que visita este site.</p>
            
