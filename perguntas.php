                <p> 
                    O Google AdWords determina a posição de seu anúncio com base em dois fatores:
                </p>
                <p>
                    - A relevância de seu anúncio e do website para os termos de pesquisa utilizados (também chamada de "Índice de qualidade");<br>
                </p>
                <p>
                    - O valor que você deseja pagar por um clique em seu anúncio (também chamado de "lance máximo de CPC").
                </p>
                <p>
                    A posição do anúncio é determinada por uma fórmula chamada Classificação do Anúncio, que classifica o anúncio com base no lance de CPC e no Índice de Qualidade. Assim, mesmo que os lances de seu concorrente sejam maiores do que o seu, você ainda poderá conseguir uma posição superior (por um preço menor) com palavras-chave e anúncios mais relevantes.
