                <summary>O que é SEO? O que é SEM? Qual devo escolher?</summary>
                <p> 
                    Otimização de mecanismos de pesquisa (SEO, na sigla em inglês) é o processo de ajustar um website para melhorar sua visibilidade em mecanismos de pesquisa nos resultados de pesquisa natural (também chamada de não paga ou orgânica).
                </p>
                <p>
                    Marketing de Busca (SEM, na sigla em inglês) é a publicidade paga exibida ao lado ou acima dos resultados de pesquisa não paga. O Google AdWords é uma forma de marketing por mecanismos de pesquisa. Anunciar no Google não afeta de forma alguma a classificação de um website nos resultados de pesquisa natural. 
                </p>
                <p>
                    Tanto a SEO quanto SEM se complementam.
                </p>
